# ====== Задание 3: Сортировка пиццы ======

print("=== Задание 3 ===")

times = [45, 15, 60, 30, 10, 90, 25, 50, 35, 20, 55, 40, 70, 5, 80]

# Часть А: Пузырёк
def bubble_sort(arr):
    a = arr.copy()
    n = len(a)
    comparisons = 0
    swaps = 0
    for i in range(n):
        for j in range(n - 1 - i):
            comparisons += 1
            if a[j] > a[j + 1]:
                a[j], a[j + 1] = a[j + 1], a[j]
                swaps += 1
        print(f"  Шаг {i+1}: {a}")
    print(f"  Всего сравнений: {comparisons}, обменов: {swaps}")
    return a

print("Пузырёк:")
bubble_sort(times)

# Часть Б: Быстрая сортировка
def quick_sort(arr):
    if len(arr) <= 1:
        return arr
    pivot = arr[len(arr) // 2]
    left = [x for x in arr if x < pivot]
    middle = [x for x in arr if x == pivot]
    right = [x for x in arr if x > pivot]
    print(f"  pivot={pivot}: {left} + {middle} + {right}")
    return quick_sort(left) + middle + quick_sort(right)

print("\nБыстрая сортировка:")
qs = quick_sort(times)
print(f"  Результат: {qs}")

