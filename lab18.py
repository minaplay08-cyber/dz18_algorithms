# Бинарный поиск товара за 5000 руб (если отсортировано по цене)
prices = sorted([p["price"] for p in products])

def binary_search(arr, target):
    left, right = 0, len(arr) - 1
    steps = 0
    while left <= right:
        steps += 1
        mid = (left + right) // 2
        print(f"  Шаг {steps}: проверяю индекс {mid} значение {arr[mid]}")
        if arr[mid] == target:
            print(f"  Нашёл за {steps} шагов!")
            return mid
        elif arr[mid] < target:
            left = mid + 1
        else:
            right = mid - 1
    return -1

print("\nБинарный поиск цены 5000:")
binary_search(prices, 5000)

